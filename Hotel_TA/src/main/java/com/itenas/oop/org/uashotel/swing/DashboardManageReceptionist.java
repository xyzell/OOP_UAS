     private void loadData(){
        receptionistService = new ReceptionistServiceImpl();
        List<Receptionist> listRecep = new ArrayList<>();
        listRecep = receptionistService.findAll();
        Object[][] objectRecep = new Object[listRecep.size()][5];
        
        
        int counter = 0;
        
        for (Receptionist receptionist : listRecep) {
            objectRecep[counter][0] = receptionist.getIdReceptionist();
            objectRecep[counter][1] = receptionist.getNamaReceptionist();
            objectRecep[counter][2] = receptionist.getNumberReceptionist();
            objectRecep[counter][3] = receptionist.getAccount().getEmail();
            objectRecep[counter][4] = receptionist.getAccount().getUsername();
            
            counter++;
        }
        tabel.setModel(new javax.swing.table.DefaultTableModel(
            objectRecep,
            new String [] {
                "ID", "Name", "Phone Number", "Email", "Account Username"
            }
        ));
     }
     
