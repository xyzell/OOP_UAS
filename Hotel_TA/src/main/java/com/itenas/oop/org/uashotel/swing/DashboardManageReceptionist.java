    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        receplogin = new ReceptionistServiceLoginImpl();
        recepDetail = new ReceptionistServiceImpl();
        receptionist = new Receptionist();
        
        try{
                    Long.valueOf(phone.getText());
                    
                    int checker = receplogin.register(username.getText(), email.getText(), password.getText());
                    receptionist.setNamaReceptionist(name.getText());
                    receptionist.setNumberReceptionist(phone.getText());
                    receptionist.setIdReceptionist(id.getText());
                    checker = recepDetail.create(receptionist);
                    
                    if(checker == 0){
                        confirm.setText("Added Successfully!");
                        emptyField();
                        loadData();
                        emptyField();
                    } else{
                        confirm.setText("Failed to Add!");
                    }
                    
                    new Timer(4_000, (e) -> {confirm.setText(null);}).start();
                    
                } catch (NumberFormatException e) {
                    if(phone.getText().isEmpty()){
                        System.out.println("Test");
                    } else {
                        JOptionPane.showMessageDialog(null, "Isi Kolom Phone Number Dengan Benar", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
            
                }
    }//GEN-LAST:event_addActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        receptionistService = new ReceptionistServiceImpl();
        receptionist = new Receptionist();
        
        receptionist.setIdReceptionist(id.getText());
        receptionist.setNamaReceptionist(name.getText());
        receptionist.setNumberReceptionist(phone.getText());
        Account acc = new Account();
        acc.setPassword(password.getText());
        receptionist.setAccount(acc);

        int tester = receptionistService.update(receptionist);
        if(tester == 0){
            confirm.setText("Updated Successfully!");
            loadData();
            emptyField();
        } else {
            confirm.setText("Failed to Update!");
            JOptionPane.showMessageDialog(null, "Password Salah", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        new Timer(4_000, (e) -> {confirm.setText(null);}).start();
        
    }//GEN-LAST:event_editActionPerformed

